def permute(value, order):
    order = [int(i) - 1 if int(i) >= 1 else int(i) for i in order]
    return np.transpose(value, axes=order)


def movstd(values, window):
    arr = np.asarray(values, dtype=float)
    w = max(int(window), 1)
    if arr.ndim != 1:
        arr = arr.reshape(-1)
    out = np.zeros_like(arr, dtype=float)
    for i in range(arr.size):
        lo = max(0, i - w // 2)
        hi = min(arr.size, i + (w - w // 2))
        out[i] = float(np.std(arr[lo:hi]))
    return out


def padarray(arr, pad_width, value=0, direction="both"):
    a = np.asarray(arr)
    if isinstance(pad_width, int):
        pads = [(pad_width, pad_width)] * a.ndim
    else:
        pw = list(pad_width)
        if pw and not isinstance(pw[0], (list, tuple)):
            pads = [(int(p), int(p)) for p in pw] + [(0, 0)] * max(0, a.ndim - len(pw))
        else:
            pads = [(int(p[0]), int(p[1])) for p in pw] + [(0, 0)] * max(0, a.ndim - len(pw))
        pads = pads[: a.ndim]
    if direction == "pre":
        pads = [(p[0], 0) for p in pads]
    elif direction == "post":
        pads = [(0, p[1]) for p in pads]
    return np.pad(a, pads, mode="constant", constant_values=value)


def roipoly(*args, **kwargs):
    # Minimal compatible implementation:
    # roipoly(image_shape, x_vertices, y_vertices) -> bool mask
    if len(args) >= 3:
        first = np.asarray(args[0])
        if first.ndim >= 2:
            shape = first.shape[:2]
        else:
            shape = tuple(int(v) for v in first[:2])
        xv = np.asarray(args[1], dtype=float).reshape(-1)
        yv = np.asarray(args[2], dtype=float).reshape(-1)
    else:
        shape = kwargs.get("shape", (0, 0))
        xv = np.asarray(kwargs.get("xv", []), dtype=float).reshape(-1)
        yv = np.asarray(kwargs.get("yv", []), dtype=float).reshape(-1)

    if len(shape) < 2 or xv.size == 0 or yv.size == 0:
        return np.zeros((int(shape[0]) if shape else 0, int(shape[1]) if len(shape) > 1 else 0), dtype=bool)

    h, w = int(shape[0]), int(shape[1])
    try:
        from matplotlib.path import Path as _MplPath  # lazy import

        yy, xx = np.mgrid[0:h, 0:w]
        pts = np.vstack((xx.ravel(), yy.ravel())).T
        poly = np.vstack((xv, yv)).T
        mask = _MplPath(poly).contains_points(pts)
        return mask.reshape((h, w))
    except Exception:  # noqa: BLE001
        # Cheap fallback: bounding box mask.
        x0, x1 = int(np.floor(np.min(xv))), int(np.ceil(np.max(xv)))
        y0, y1 = int(np.floor(np.min(yv))), int(np.ceil(np.max(yv)))
        mask = np.zeros((h, w), dtype=bool)
        x0 = max(0, x0)
        y0 = max(0, y0)
        x1 = min(w - 1, x1)
        y1 = min(h - 1, y1)
        if x1 >= x0 and y1 >= y0:
            mask[y0 : y1 + 1, x0 : x1 + 1] = True
        return mask


