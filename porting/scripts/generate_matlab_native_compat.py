def _iter_files(roots: list[Path], pattern: str) -> list[Path]:
    out: list[Path] = []
    for root in roots:
        if root.exists():
            out.extend([p for p in root.rglob(pattern) if p.is_file()])
    dedup = {p.resolve(): p for p in out}
    return sorted(dedup.values(), key=lambda p: str(p))


def _defined_symbols(py_files: list[Path]) -> set[str]:
    symbols: set[str] = set()
    for path in py_files:
        try:
            tree = ast.parse(path.read_text(encoding="utf-8", errors="ignore"))
        except SyntaxError:
            continue
        for node in getattr(tree, "body", []):
            if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef, ast.ClassDef)):
                symbols.add(node.name)
    return symbols


def _extract_candidates_from_python_todos(py_files: list[Path]) -> set[str]:
    names: set[str] = set()
    for path in py_files:
        text = path.read_text(encoding="utf-8", errors="ignore")
        for line in text.splitlines():
            m = MATLAB_TODO_RE.search(line)
            if not m:
                continue
            payload = m.group(1)
            for call in CALL_RE.findall(payload):
                names.add(call)
    return names


def _is_valid_name(name: str) -> bool:
    if not VALID_IDENT_RE.match(name):
        return False
    if keyword.iskeyword(name):
        return False
    if name.lower() in MATLAB_KEYWORDS:
        return False
    if name in dir(builtins):
        return False
    return True


def _is_plausible_matlab_native_name(name: str) -> bool:
    if name in SEED_FUNCTIONS:
        return True
    # Keep only simple lowercase native-like names beyond curated seed set.
    if any(ch.isupper() for ch in name):
        return False
    if "_" in name:
        return False
    project_prefixes = ("bm", "bca", "mle", "dh", "ml", "test", "run")
    if any(name.startswith(prefix) for prefix in project_prefixes):
        return False
    return True


def _generic_wrapper(name: str) -> str:
    return f'''
def {name}(*args, **kwargs):
    fn = getattr(np, "{name}", None)
    if callable(fn):
        try:
            return fn(*args, **kwargs)
        except Exception:  # noqa: BLE001
            pass
    return None
'''.strip(
        "\n"
    )


def _build_module_text(extra_names: list[str]) -> str:
    chunks = [MODULE_HEADER.rstrip()]
    for name in extra_names:
        if name in SEED_FUNCTIONS:
            continue
        chunks.append("")
        chunks.append(_generic_wrapper(name))
    chunks.append("")
    chunks.append("__all__ = [name for name in globals() if not name.startswith('_')]")
    chunks.append("")
    return "\n".join(chunks)


def generate_native_compat(roots: list[Path], repo_root: Path, apply: bool, include_discovered: bool) -> dict:
    py_files = _iter_files(roots, "*.py")
    defined = _defined_symbols(py_files)
    todo_candidates = _extract_candidates_from_python_todos(py_files)

    merged = set(SEED_FUNCTIONS)
    if include_discovered:
        merged.update(todo_candidates)
    merged = {name for name in merged if _is_valid_name(name)}
    merged = {name for name in merged if _is_plausible_matlab_native_name(name)}
    missing = sorted(name for name in merged if name not in defined)

    compat_dir = repo_root / "third_part" / "matlab_compat"
    compat_file = compat_dir / "matlab_native.py"
    init_root = repo_root / "third_part" / "__init__.py"
    init_compat = compat_dir / "__init__.py"

    module_text = _build_module_text(missing)
    old_text = compat_file.read_text(encoding="utf-8", errors="ignore") if compat_file.exists() else ""
    changed = old_text != module_text

    if apply:
        compat_dir.mkdir(parents=True, exist_ok=True)
        if not init_root.exists():
            init_root.write_text("", encoding="utf-8")
        if not init_compat.exists():
            init_compat.write_text("", encoding="utf-8")
        compat_file.write_text(module_text, encoding="utf-8")

    return {
        "mode": "apply" if apply else "dry-run",
        "roots": [str(p) for p in roots],
        "python_files_scanned": len(py_files),
        "todo_candidate_functions": len(todo_candidates),
        "include_discovered": include_discovered,
        "compat_functions_generated": len(missing),
        "compat_file": str(compat_file),
        "compat_changed": changed,
        "generated_function_names": missing,
    }


def main() -> int:
    parser = argparse.ArgumentParser(description="Generate MATLAB native compatibility helpers.")
    parser.add_argument(
        "--roots",
        default="src,demo,tests,third_part",
        help="Comma-separated roots to scan for TODO(matlab-*) markers.",
    )
    parser.add_argument("--repo-root", default="../../", help="Repository root.")
    parser.add_argument("--apply", action="store_true", help="Apply changes (default dry-run).")
    parser.add_argument(
        "--include-discovered",
        action="store_true",
        help="Also generate wrappers for discovered TODO(matlab-*) function names.",
    )
    parser.add_argument("--output", default="../reports/matlab_native_compat_report.json", help="Output report path.")
    parser.add_argument("--summary-only", action="store_true", help="Print compact summary.")
    args = parser.parse_args()

    base = Path(__file__).resolve().parent
    repo_root = (base / args.repo_root).resolve()
    roots = [(repo_root / token.strip()).resolve() for token in args.roots.split(",") if token.strip()]

    report = generate_native_compat(
        roots=roots,
        repo_root=repo_root,
        apply=args.apply,
        include_discovered=args.include_discovered,
    )
    output = (base / args.output).resolve()
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(json.dumps(report, indent=2), encoding="utf-8")

    if args.summary_only:
        compact = {
            "mode": report["mode"],
            "python_files_scanned": report["python_files_scanned"],
            "todo_candidate_functions": report["todo_candidate_functions"],
            "compat_functions_generated": report["compat_functions_generated"],
            "compat_changed": report["compat_changed"],
        }
        print(json.dumps(compact, indent=2))
    else:
        print(json.dumps(report, indent=2))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
